/*

Incremento e decremento

x++ incrementa depois da operação e retonar o valor
++x incrementa antes da operação e retonar o valor
--x decrementa antes da operação e retonar o valor
x-- decrementa depois da operação e retonar o valor
*/

/*

Operadores de Igualdade e Relacionais

==   compara se o dado são iguais
===  compara se o dado são iguais e do mesmo tipo
!=   compara se o dado e diferente
!==  compara se o dado e diferente e se são de tipos diferentes

---------------------

<   menor que
>   maior que
>=  maior ou igual que
<=  menor ou igual que 


*/

